let lightColor = "green";

//Check if the light color is "red".
if (lightColor === "red") {
    console.log("The light color is red.");
} else {
    console.log("The light color is not red.");
}

//Check if the light color is "green".
if (lightColor === "green") {
    console.log("The light color is green.");
} else {
    console.log("The light color is not green.");
}

//Check if the light color is NOT "yellow".
if (lightColor !== "yellow") {
    console.log("The light color is not yellow.");
} else {
    console.log("The light color is yellow.");
}
