let battery = 15;

//Check if the battery is below 20.
if (battery < 20) {
    console.log("battery is below 20");
} else {
    console.log("battery is above 20");
}

//Check if the battery is exactly 100.
if (battery === 100) {
    console.log("battery is exactly 100");
} else {
    console.log("battery is not exactly 100");
}


//Check if the battery is NOT 0.
if (battery !== 0) {
    console.log("battery is not 0");
}   else {
    console.log("battery is 0");
}

