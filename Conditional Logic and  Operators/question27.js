let waterLevel = 15;

//Check if the water level is below 20.
if (waterLevel < 20) {
    console.log("water level is below 20");
} else {
    console.log("water level is above 20");
}   

//Check if the tank is full.
if (waterLevel === 15) {
    console.log("water level is full");
} else {
    console.log("water level is not full");
} 


//Check if the water level is NOT 0.
if (waterLevel !== 0) {
    console.log("water level is not 0");
} else {
    console.log("water level is 0");
}
