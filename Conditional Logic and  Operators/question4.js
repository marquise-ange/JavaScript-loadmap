//Check if attendance is 90 or more.
let attendance = 92;

if (attendance>=90){
    console.log("attendance is good");
}else{
    console.log("attendance is poor");
}

//Check if attendance is below 75.
if (attendance < 75) {
    console.log("attendance is very poor");
}else{
    console.log("attendance is very good");
}

//Check if attendance is NOT exactly 100.
if (attendance !== 100) {
    console.log("attendance is not exactly 100");
} else {
    console.log("attendance is exactly 100");
}
