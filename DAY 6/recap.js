// switch statement
let day = "Monday";

switch (day) {
    case "Monday":
        console.log("Theme is red");
        break;
    case "Tuesday":
        console.log("Theme is blue");
        break;
    case "Wednesday":
        console.log("Theme is green");
        break;
    case "Thursday":
        console.log("Theme is yellow");
        break;
    case "Friday":
        console.log("Theme is purple");
        break;
    default:
        console.log("Invalid day")
}

